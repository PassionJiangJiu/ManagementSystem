package com.pojo;

public class Dept {
    private Integer deptid;

    private String deptname;

    private String deptstyle;

    private String deptmoney;

    private String deptcode;

    public Integer getDeptid() {
        return deptid;
    }

    public void setDeptid(Integer deptid) {
        this.deptid = deptid;
    }

    public String getDeptname() {
        return deptname;
    }

    public void setDeptname(String deptname) {
        this.deptname = deptname;
    }

    public String getDeptstyle() {
        return deptstyle;
    }

    public void setDeptstyle(String deptstyle) {
        this.deptstyle = deptstyle;
    }

    public String getDeptmoney() {
        return deptmoney;
    }

    public void setDeptmoney(String deptmoney) {
        this.deptmoney = deptmoney;
    }

    public String getDeptcode() {
        return deptcode;
    }

    public void setDeptcode(String deptcode) {
        this.deptcode = deptcode;
    }
}
